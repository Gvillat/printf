#include "../includes/ft_printf.h"

int	ft_strequ(char const *s1, char const *s2)
{
	if (!(char*)s1 || !(char*)s2)
		return (0);
	while (*s1 || *s2)
	{
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
	}
	return (1);
}
