#include "../includes/ft_printf.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;

	i = 0;
	if ((char*)s && *f)
		while (s[i])
		{
			f(i, &(s[i]));
			i++;
		}
}
