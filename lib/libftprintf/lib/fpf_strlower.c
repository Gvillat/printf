#include "../includes/fpf_printf.h"

void	*fpf_strlower(char *s)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] >= 'A' && s[i] <= 'Z')
			s[i] = fpf_tolower(s[i]);
		i++;
	}
	return (s);
}
